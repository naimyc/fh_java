package com;

class Kunde {
	// Attribute
	private String name, vorname;

	// set-Methoden
	void setName(String name) {
		this.name = name;
	}

	void setVorname(String vorname) {
		this.vorname = vorname;
	}

	// get-Methoden
	public String getName() {
		return name;
	}

	public String getVorname() {
		return vorname;
	}
}
